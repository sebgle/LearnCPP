/*

Containers
    - provide data structures to store and organize data

    
    1. Vector   
        - dynamic arrays that can resize themselves as needed
        - store elements in a contiguous memory location, allowing fast random access using indices

    2. List
        - doubly-linked list that allows elements to be inserted or removed from any position in constant time
        - does not support random access
        - better than vectors for scenarios where you need to insert or remove elements in the middle frequently

    3. Map
        - associative container that stores key-value pairs
        - supports the retrieval of values based on their keys
        - keys are sorted in ascending order by default

    4. Unordered Map
        - stores key-value pairs, but it is implemented using a hash table
        - unordered_map has faster average-case performance compared to map

*/