/*

Object Oriented Programming
    - a programming paradigm that uses objects (instances of classes) to perform operations and interact with eachother


    Classes
        - blueprint for creating objects
        - defines the structure (data members) & behavior (function members) for a type of object
    
    Encapsulation
        - the concept of bundling data and functions that operate on data within a single unit
        - helps hide the internal implementation details of a class and expose only the necessary information and functionalities
        - can use access specifiers (public, private, protected) to control visibility and accessibility of class members

    Inheritance
        - concept of deriving new classes from existing ones 
        - enables code reusability and organization
        - achieved through using a colon ( : ) followed by the base class' access specifier and name

    Polymporphism
        - allows you to use a single interface to represent different types
        - mainly achieved through function overloading, virtual functions, and overriding

*/